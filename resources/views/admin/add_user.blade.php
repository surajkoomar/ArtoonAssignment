<script>
	function add_user(){
		var userName = $("#name").val();
		var userEmail = $("#email").val();
		var userPassword = $("#password").val();
		
		$.ajax( {
			headers: {
       		 'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
   		 },
            url:'{{url("saveUser")}}',
            method:'POST',
            data:{
            	userName:userName,
            	userEmail:userEmail,
            	userPassword:userPassword,
            },
            success: function(data) { 
                alert(data.msg);
            },  
            
         }); 

	}
</script>	
